    public static void addRandomDecorations(char[][] maze, int count) {
        for (int i = 0; i < count; i++) {
            int rx = random.nextInt(maze.length);
            int ry = random.nextInt(maze[0].length);
            if (maze[rx][ry] == '.' && !(rx == 4 && ry == 2) && !(rx == 8 && ry == 8) && !(rx == 8 && ry == 7)) {
                maze[rx][ry] = 'T';
            }
        }
    }
